// CONTRACTS FILE
// ============================================================================

function generateContractsFile(module: AriaModule, contracts: ContractDef[]): string {
  const lines: string[] = [
    `// Generated by ARIA compiler v0.1.0`,
    `// Source: ${kebab(module.name)}.aria`,
    `// DO NOT EDIT — regenerate with: aria gen ${kebab(module.name)}.aria`,
    ``,
    `use serde::{Serialize, Deserialize};`,
    `use super::*;`,
    ``,
  ];

  for (const c of contracts) {
    lines.push(...generateContract(c));
    lines.push(``);
  }

  return lines.join("\n");
}

function generateContract(c: ContractDef): string[] {
  const lines: string[] = [];
  const name = c.name;
  const snakeName = toSnakeCase(name);

  // Input struct
  lines.push(`#[derive(Debug, Clone, Serialize, Deserialize)]`);
  lines.push(`pub struct ${name}Input {`);
  for (const inp of c.inputs) {
    lines.push(`    pub ${toRustFieldName(inp.name)}: ${typeRefToRust(inp.type)},`);
  }
  lines.push(`}`);
  lines.push(``);

  // Success struct
  lines.push(`#[derive(Debug, Clone, Serialize, Deserialize)]`);
  lines.push(`pub struct ${name}Success {}`);
  lines.push(``);

  // Error enum
  if (c.onFailure.length > 0) {
    lines.push(`#[derive(Debug, Clone, Serialize, Deserialize)]`);
    lines.push(`pub enum ${name}Error {`);
    for (const f of c.onFailure) {
      lines.push(`    ${f.return.type},`);
    }
    lines.push(`}`);
    lines.push(``);
  }

  // Async function stub
  const returnOk = `${name}Success`;
  const returnErr = c.onFailure.length > 0 ? `${name}Error` : `String`;
  lines.push(`pub async fn ${snakeName}(input: ${name}Input) -> Result<${returnOk}, ${returnErr}> {`);
  lines.push(`    // TODO: implement`);
  lines.push(`    unimplemented!()`);
  lines.push(`}`);
  lines.push(``);

  // Precondition checker
  lines.push(`pub fn ${snakeName}_requires(input: &${name}Input) -> bool {`);
  for (const r of c.requires) {
    lines.push(`    // ${r.expression}`);
  }
  lines.push(`    true`);
  lines.push(`}`);

  return lines;
}

// ============================================================================
// BEHAVIORS FILE
// ============================================================================

function generateBehaviorsFile(module: AriaModule, behaviors: BehaviorDef[]): string {
  const lines: string[] = [
    `// Generated by ARIA compiler v0.1.0`,
    `// Source: ${kebab(module.name)}.aria`,
    `// DO NOT EDIT — regenerate with: aria gen ${kebab(module.name)}.aria`,
    ``,
  ];

  for (const b of behaviors) {
    lines.push(...generateBehavior(b));
    lines.push(``);
  }

  return lines.join("\n");
}

function generateBehavior(b: BehaviorDef): string[] {
  const lines: string[] = [];
  const name = b.name;
  const snakeName = toSnakeCase(name);
  const constName = toSnakeCase(name).toUpperCase();

  // Doc comment + invariants as /// doc comments on the state enum
  // Sanitize newlines (/// comments are line-scoped) and backticks (code fences)
  const sanitizeForRustDoc = (s: string): string =>
    s.replace(/\n/g, " ").replace(/`/g, "'");
  if (b.docComment) {
    lines.push(`/// ${sanitizeForRustDoc(b.docComment)}`);
  }
  if (b.invariants.length > 0) {
    if (b.docComment) lines.push(`///`);
    lines.push(`/// # Invariants`);
    for (const inv of b.invariants) {
      lines.push(`/// - \`${sanitizeForRustDoc(inv.expression)}\``);
    }
  }

  // State enum
  lines.push(`#[derive(Debug, Clone, Copy, PartialEq, Eq)]`);
  lines.push(`pub enum ${name}State {`);
  for (const s of b.states) {
    lines.push(`    ${toPascalCase(s.name)},`);
  }
  lines.push(`}`);
  lines.push(``);

  // Export invariants as a static slice for runtime inspection
  if (b.invariants.length > 0) {
    lines.push(`pub const ${constName}_INVARIANTS: &[&str] = &[`);
    for (const inv of b.invariants) {
      lines.push(`    ${JSON.stringify(inv.expression)},`);
    }
    lines.push(`];`);
    lines.push(``);
  }

  // Initial state constant
  const initialPascal = toPascalCase(b.initialState);
  lines.push(`pub const ${constName}_INITIAL: ${name}State = ${name}State::${initialPascal};`);
  lines.push(``);

  // Transition function
  lines.push(`pub fn can_${snakeName}_transition(from: ${name}State, to: ${name}State) -> bool {`);
  lines.push(`    match (from, to) {`);
  for (const t of b.transitions) {
    const fromPascal = toPascalCase(t.from);
    const toPascal = toPascalCase(t.to);
    lines.push(`        (${name}State::${fromPascal}, ${name}State::${toPascal}) => true,`);
  }
  lines.push(`        _ => false,`);
  lines.push(`    }`);
  lines.push(`}`);

  return lines;
}

// ============================================================================
// HELPERS
// ============================================================================

function toRustFieldName(name: string): string {
  return name;
}

function toPascalCase(s: string): string {
  return s.split(/[_-]/).map((w) => w.charAt(0).toUpperCase() + w.slice(1)).join("");
}

function toSnakeCase(s: string): string {
  return s.replace(/([a-z])([A-Z])/g, "$1_$2").toLowerCase();
}

function kebab(s: string): string {
  return s
    .replace(/([a-z])([A-Z])/g, "$1-$2")
    .replace(/[\s_]+/g, "-")
    .toLowerCase();
}
