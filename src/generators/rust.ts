/**
 * ARIA Rust Code Generator
 * Compiles ARIA AST into Rust structs, enums, and state machine code
 */

import type {
  AriaModule,
  TypeDef,
  ContractDef,
  BehaviorDef,
  WhereClause,
} from "../ast.js";

export interface GeneratedFile {
  path: string;
  content: string;
}

export function generateRust(module: AriaModule): GeneratedFile[] {
  const files: GeneratedFile[] = [];

  const types = module.body.filter((n): n is TypeDef => n.kind === "type");
  const contracts = module.body.filter((n): n is ContractDef => n.kind === "contract");
  const behaviors = module.body.filter((n): n is BehaviorDef => n.kind === "behavior");

  files.push({
    path: `${kebab(module.name)}-types.rs`,
    content: generateTypesFile(module, types),
  });

  if (contracts.length > 0) {
    files.push({
      path: `${kebab(module.name)}-contracts.rs`,
      content: generateContractsFile(module, contracts),
    });
  }

  if (behaviors.length > 0) {
    files.push({
      path: `${kebab(module.name)}-behaviors.rs`,
      content: generateBehaviorsFile(module, behaviors),
    });
  }

  return files;
}

// ============================================================================
// TYPES FILE
// ============================================================================

function generateTypesFile(module: AriaModule, types: TypeDef[]): string {
  const lines: string[] = [
    `// Generated by ARIA compiler v0.1.0`,
    `// Source: ${kebab(module.name)}.aria`,
    `// DO NOT EDIT — regenerate with: aria gen ${kebab(module.name)}.aria`,
    ``,
    `use serde::{Serialize, Deserialize};`,
    ``,
  ];

  for (const t of types) {
    if (t.docComment) {
      lines.push(`/// ${t.docComment}`);
    }
    lines.push(...generateTypeDef(t));
    lines.push(``);
  }

  return lines.join("\n");
}

function generateTypeDef(t: TypeDef): string[] {
  const lines: string[] = [];

  switch (t.base) {
    case "Integer": {
      lines.push(`#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]`);
      lines.push(`pub struct ${t.name}(pub i64);`);
      lines.push(``);
      lines.push(`impl ${t.name} {`);
      lines.push(`    pub fn new(v: i64) -> Result<Self, String> {`);
      for (const w of t.whereClauses) {
        const check = rustValidationCheck(w);
        lines.push(`        if !(${check}) { return Err(${JSON.stringify(w.expression.trim())}.into()); }`);
      }
      lines.push(`        Ok(${t.name}(v))`);
      lines.push(`    }`);
      lines.push(`}`);
      break;
    }
    case "Decimal": {
      lines.push(`#[derive(Debug, Clone, Copy, PartialEq, Serialize, Deserialize)]`);
      lines.push(`pub struct ${t.name}(pub f64);`);
      lines.push(``);
      lines.push(`impl ${t.name} {`);
      lines.push(`    pub fn new(v: f64) -> Result<Self, String> {`);
      for (const w of t.whereClauses) {
        const check = rustValidationCheck(w);
        lines.push(`        if !(${check}) { return Err(${JSON.stringify(w.expression.trim())}.into()); }`);
      }
      lines.push(`        Ok(${t.name}(v))`);
      lines.push(`    }`);
      lines.push(`}`);
      break;
    }
    case "String": {
      lines.push(`#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]`);
      lines.push(`pub struct ${t.name}(pub String);`);
      lines.push(``);
      lines.push(`impl ${t.name} {`);
      lines.push(`    pub fn new(v: String) -> Result<Self, String> {`);
      for (const w of t.whereClauses) {
        const check = rustStringValidationCheck(w);
        lines.push(`        if !(${check}) { return Err(${JSON.stringify(w.expression.trim())}.into()); }`);
      }
      lines.push(`        Ok(${t.name}(v))`);
      lines.push(`    }`);
      lines.push(`}`);
      break;
    }
    case "Boolean": {
      lines.push(`#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]`);
      lines.push(`pub struct ${t.name}(pub bool);`);
      break;
    }
    case "DateTime": {
      lines.push(`#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]`);
      lines.push(`pub struct ${t.name}(pub String);`);
      break;
    }
    case "Record": {
      // Phase 7.2: storage fields go into the struct; computed fields become
      // impl methods the consumer must fill in.
      const storageFields = (t.fields || []).filter((f) => !f.computed);
      const computedFields = (t.fields || []).filter((f) => f.computed);

      lines.push(`#[derive(Debug, Clone, Serialize, Deserialize)]`);
      lines.push(`pub struct ${t.name} {`);
      for (const f of storageFields) {
        const rustType = typeRefToRust(f.type);
        lines.push(`    pub ${toRustFieldName(f.name)}: ${rustType},`);
      }
      lines.push(`}`);

      if (computedFields.length > 0) {
        lines.push(``);
        lines.push(`impl ${t.name} {`);
        for (const f of computedFields) {
          const rustType = typeRefToRust(f.type);
          const sanitized = (f.computed || "").replace(/\n/g, " ").replace(/`/g, "'");
          lines.push(`    /// Computed: \`${sanitized}\``);
          lines.push(`    pub fn ${toRustFieldName(f.name)}(&self) -> ${rustType} {`);
          lines.push(`        todo!("Computed field '${f.name}': ${sanitized.replace(/"/g, '\\"')}")`);
          lines.push(`    }`);
        }
        lines.push(`}`);
      }
      break;
    }
    case "Enum": {
      lines.push(`#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]`);
      lines.push(`#[serde(rename_all = "snake_case")]`);
      lines.push(`pub enum ${t.name} {`);
      if (t.variants) {
        for (const v of t.variants) {
          lines.push(`    ${toPascalCase(v.name)},`);
        }
      }
      lines.push(`}`);
      break;
    }
    case "List": {
      const elemType = t.elementType ? typeRefToRust(t.elementType) : "serde_json::Value";
      lines.push(`pub type ${t.name} = Vec<${elemType}>;`);
      break;
    }
  }

  return lines;
}

function rustValidationCheck(w: WhereClause): string {
  const expr = w.expression.trim();

  // self > N
  const gtMatch = expr.match(/^self\s*>\s*(\d+)$/);
  if (gtMatch) return `v > ${gtMatch[1]}`;

  // self >= N
  const gteMatch = expr.match(/^self\s*>=\s*(\d+)$/);
  if (gteMatch) return `v >= ${gteMatch[1]}`;

  // self < N
  const ltMatch = expr.match(/^self\s*<\s*(\d+)$/);
  if (ltMatch) return `v < ${ltMatch[1]}`;

  // self <= N (allow underscores in numbers)
  const lteMatch = expr.match(/^self\s*<=\s*([\d_]+)$/);
  if (lteMatch) return `v <= ${lteMatch[1].replace(/_/g, "")}`;

  // self == N
  const eqMatch = expr.match(/^self\s*==\s*(\d+)$/);
  if (eqMatch) return `v == ${eqMatch[1]}`;

  // Fallback: emit TODO comment with safe expression
  const safeExpr = expr.replace(/\*\//g, "* /");
  return `{ /* TODO: ${safeExpr} */ true }`;
}

function rustStringValidationCheck(w: WhereClause): string {
  const expr = w.expression.trim();

  // length(self) <= N
  const lenLteMatch = expr.match(/^length\s*\(\s*self\s*\)\s*<=\s*([\d_]+)$/);
  if (lenLteMatch) return `v.len() <= ${lenLteMatch[1].replace(/_/g, "")}`;

  // length(self) >= N
  const lenGteMatch = expr.match(/^length\s*\(\s*self\s*\)\s*>=\s*(\d+)$/);
  if (lenGteMatch) return `v.len() >= ${lenGteMatch[1]}`;

  // length(self) == N
  const lenEqMatch = expr.match(/^length\s*\(\s*self\s*\)\s*==\s*(\d+)$/);
  if (lenEqMatch) return `v.len() == ${lenEqMatch[1]}`;

  // self matches /regex/
  const regexMatch = expr.match(/^self\s+matches\s+\/(.+)\/$/);
  if (regexMatch) {
    const safeRegex = regexMatch[1].replace(/\\/g, "\\\\").replace(/"/g, '\\"');
    return `regex::Regex::new("${safeRegex}").unwrap().is_match(&v)`;
  }

  // Fallback
  const safeExpr = expr.replace(/\*\//g, "* /");
  return `{ /* TODO: ${safeExpr} */ true }`;
}

function typeRefToRust(ref: { name: string; isOptional?: boolean }): string {
  const primitives: Record<string, string> = {
    Integer: "i64",
    Decimal: "f64",
    String: "String",
    Boolean: "bool",
    DateTime: "String",
  };
  const base = primitives[ref.name] ?? ref.name;
  return ref.isOptional ? `Option<${base}>` : base;
}

