function generateBehaviorsFile(module: AriaModule, behaviors: BehaviorDef[]): string {
  const lines: string[] = [
    `// Generated by ARIA compiler v0.1.0`,
    `// Source: ${kebab(module.name)}.aria`,
    ``,
  ];

  for (const b of behaviors) {
    lines.push(...generateBehavior(b));
    lines.push(``);
  }

  return lines.join("\n");
}

function generateBehavior(b: BehaviorDef): string[] {
  const lines: string[] = [];
  const name = b.name;

  // Doc comment + invariants as JSDoc on the behavior group
  // Sanitize `*/` to prevent premature comment closure (JSDoc injection)
  const sanitizeForJSDoc = (s: string): string =>
    s.replace(/\*\//g, "* /").replace(/\n/g, " ");
  if (b.docComment || b.invariants.length > 0) {
    lines.push(`/**`);
    if (b.docComment) {
      lines.push(` * ${sanitizeForJSDoc(b.docComment)}`);
    }
    if (b.invariants.length > 0) {
      if (b.docComment) lines.push(` *`);
      for (const inv of b.invariants) {
        lines.push(` * @invariant ${sanitizeForJSDoc(inv.expression)}`);
      }
    }
    lines.push(` */`);
  }

  // State enum
  const stateNames = b.states.map((s) => s.name);
  lines.push(`export const ${name}States = [${stateNames.map((s) => `"${s}"`).join(", ")}] as const;`);
  lines.push(`export type ${name}State = (typeof ${name}States)[number];`);
  lines.push(``);

  // Export invariants as string array for runtime inspection / documentation
  if (b.invariants.length > 0) {
    lines.push(`/** @internal Invariant expressions for documentation/tooling. Format may change. */`);
    lines.push(`export const ${name}Invariants: readonly string[] = [`);
    for (const inv of b.invariants) {
      lines.push(`  ${JSON.stringify(inv.expression)},`);
    }
    lines.push(`] as const;`);
    lines.push(``);
  }

  // Transition map
  lines.push(`export const ${name}Transitions: Record<${name}State, ${name}State[]> = {`);
  const transMap: Record<string, string[]> = {};
  for (const s of stateNames) {
    transMap[s] = [];
  }
  for (const t of b.transitions) {
    if (transMap[t.from]) {
      transMap[t.from].push(t.to);
    }
  }
  for (const [state, targets] of Object.entries(transMap)) {
    lines.push(`  ${state}: [${targets.map((t) => `"${t}"`).join(", ")}],`);
  }
  lines.push(`};`);
  lines.push(``);

  // Forbidden transitions
  if (b.forbidden.length > 0) {
    lines.push(`export const ${name}Forbidden: [${name}State, ${name}State][] = [`);
    for (const f of b.forbidden) {
      const comment = f.docComment ? ` // ${f.docComment}` : "";
      lines.push(`  ["${f.from}", "${f.to}"],${comment}`);
    }
    lines.push(`];`);
    lines.push(``);
  }

  // Transition validator
  lines.push(`export function can${name}Transition(from: ${name}State, to: ${name}State): boolean {`);
  lines.push(`  return ${name}Transitions[from].includes(to);`);
  lines.push(`}`);
  lines.push(``);

  // Initial state
  lines.push(`export const ${name}InitialState: ${name}State = "${b.initialState}";`);

  return lines;
}

