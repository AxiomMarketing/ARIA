function kebab(s: string): string {
  return s
    .replace(/([a-z])([A-Z])/g, "$1-$2")
    .replace(/[\s_]+/g, "-")
    .toLowerCase();
}

function camelCase(s: string): string {
  if (!s) return s;
  const first = s[0].toLowerCase();
  return first + s.slice(1);
}

function inferTsType(val: string): string {
  if (/\b(balance|amount|total|price|count|quantity)\b/.test(val)) return "number";
  if (/\b(id|email|name|token|address|phone)\b/.test(val)) return "string";
  if (/\b(at|date|time|timestamp)\b/.test(val)) return "Date";
  if (/\b(is_|has_|can_|active|enabled|verified)\b/.test(val)) return "boolean";
  return "unknown";
}
