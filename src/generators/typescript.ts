/**
 * ARIA TypeScript + Zod Code Generator
 * Compiles ARIA AST into TypeScript types, Zod schemas, guard functions, and test stubs
 */

import type {
  AriaModule,
  TypeDef,
  ContractDef,
  BehaviorDef,
  RecordField,
  EnumVariant,
  WhereClause,
  Example,
  FailureCase,
  Assertion,
  Step,
} from "../ast.js";

export interface GeneratedFile {
  path: string;
  content: string;
}

export function generateTypeScript(module: AriaModule): GeneratedFile[] {
  const files: GeneratedFile[] = [];

  const types = module.body.filter((n): n is TypeDef => n.kind === "type");
  const contracts = module.body.filter((n): n is ContractDef => n.kind === "contract");
  const behaviors = module.body.filter((n): n is BehaviorDef => n.kind === "behavior");

  // Main types + schemas file
  files.push({
    path: `${kebab(module.name)}.types.ts`,
    content: generateTypesFile(module, types),
  });

  // Contracts file
  if (contracts.length > 0) {
    files.push({
      path: `${kebab(module.name)}.contracts.ts`,
      content: generateContractsFile(module, contracts),
    });
  }

  // Behaviors file
  if (behaviors.length > 0) {
    files.push({
      path: `${kebab(module.name)}.behaviors.ts`,
      content: generateBehaviorsFile(module, behaviors),
    });
  }

  // Test file
  if (contracts.some((c) => c.examples.length > 0)) {
    files.push({
      path: `${kebab(module.name)}.test.ts`,
      content: generateTestFile(module, contracts),
    });
  }

  return files;
}

