function generateContractsFile(module: AriaModule, contracts: ContractDef[]): string {
  const lines: string[] = [
    `// Generated by ARIA compiler v0.1.0`,
    `// Source: ${kebab(module.name)}.aria`,
    `// DO NOT EDIT — regenerate with: aria gen ${kebab(module.name)}.aria`,
    ``,
    `import type * as T from "./${kebab(module.name)}.types.js";`,
    ``,
  ];

  for (const c of contracts) {
    lines.push(...generateContract(c));
    lines.push(``);
  }

  return lines.join("\n");
}

function generateContract(c: ContractDef): string[] {
  const lines: string[] = [];
  const name = c.name;
  const camel = camelCase(name);

  // Doc comment
  if (c.docComment || c.deprecated) {
    lines.push(`/**`);
    if (c.docComment) {
      lines.push(` * ${c.docComment}`);
    }
    if (c.deprecated) {
      lines.push(` *`);
      lines.push(` * @deprecated ${c.deprecated}`);
    }
    if (c.requires.length > 0) {
      lines.push(` *`);
      lines.push(` * @requires`);
      for (const r of c.requires) {
        lines.push(` *   - ${r.expression}`);
      }
    }
    if (c.ensures.length > 0) {
      lines.push(` *`);
      lines.push(` * @ensures`);
      for (const e of c.ensures) {
        lines.push(` *   - ${e.expression}`);
      }
    }
    lines.push(` */`);
  }

  // Input interface
  lines.push(`export interface ${name}Input {`);
  for (const inp of c.inputs) {
    lines.push(`  ${inp.name}: T.${inp.type.name};`);
  }
  lines.push(`}`);
  lines.push(``);

  // Error types
  if (c.onFailure.length > 0) {
    for (const f of c.onFailure) {
      lines.push(`export interface ${f.return.type} {`);
      lines.push(`  kind: "${f.return.type}";`);
      if (f.return.fields) {
        for (const [key, val] of Object.entries(f.return.fields)) {
          lines.push(`  ${key}: ${inferTsType(val)};`);
        }
      }
      lines.push(`}`);
      lines.push(``);
    }
  }

  // Result type
  const errorTypes = c.onFailure.map((f) => f.return.type);
  if (errorTypes.length > 0) {
    lines.push(`export type ${name}Result =`);
    lines.push(`  | { success: true; [key: string]: unknown }`);
    for (const errType of errorTypes) {
      lines.push(`  | ${errType}`);
    }
    lines.push(`;`);
  } else {
    lines.push(`export type ${name}Result = { success: true; [key: string]: unknown };`);
  }
  lines.push(``);

  // Precondition guard
  lines.push(`/** Precondition guard — validates requires clauses */`);
  lines.push(`export function ${camel}Requires(input: ${name}Input): boolean {`);
  if (c.requires.length > 0) {
    lines.push(`  // ${c.requires.map((r) => r.expression).join("; ")}`);
  }
  lines.push(`  return true; // TODO: implement requires checks`);
  lines.push(`}`);
  lines.push(``);

  // Postcondition checker
  lines.push(`/** Postcondition checker — validates ensures clauses */`);
  lines.push(`export function ${camel}Ensures(`);
  lines.push(`  input: ${name}Input,`);
  lines.push(`  result: ${name}Result,`);
  lines.push(`): boolean {`);
  if (c.ensures.length > 0) {
    lines.push(`  // ${c.ensures.map((e) => e.expression).join("; ")}`);
  }
  lines.push(`  return true; // TODO: implement ensures checks`);
  lines.push(`}`);
  lines.push(``);

  // Implementation stub (or dispatcher for Phase 7.3 dispatch contracts)
  if (c.dispatch) {
    lines.push(`/**`);
    lines.push(` * Dispatcher — routes to sub-contracts based on \`${c.dispatch.field}\`.`);
    lines.push(` */`);
    lines.push(`export async function ${camel}(`);
    lines.push(`  input: ${name}Input,`);
    lines.push(`): Promise<${name}Result> {`);
    lines.push(`  switch (input.${c.dispatch.field}) {`);
    for (const dc of c.dispatch.cases) {
      const targetCamel = camelCase(dc.contract);
      lines.push(`    case "${dc.value}":`);
      lines.push(`      return ${targetCamel}(input as any);`);
    }
    lines.push(`    default:`);
    lines.push(`      throw new Error(\`Unknown ${c.dispatch.field}: \${input.${c.dispatch.field}}\`);`);
    lines.push(`  }`);
    lines.push(`}`);
  } else {
    lines.push(`/** Implementation — AI generates this from the spec */`);
    lines.push(`export async function ${camel}(`);
    lines.push(`  input: ${name}Input,`);
    lines.push(`): Promise<${name}Result> {`);

    if (c.steps && c.steps.length > 0) {
      lines.push(`  // Orchestration steps:`);
      for (const step of c.steps) {
        lines.push(`  // ${step.number}. ${step.contract}(${Object.values(step.inputs).join(", ")}) -> ${step.resultName}`);
      }
    }

    lines.push(`  throw new Error("Not implemented — generate with: aria implement");`);
    lines.push(`}`);
  }

  return lines;
}

