// TYPES FILE
// ============================================================================

function generateTypesFile(module: AriaModule, types: TypeDef[]): string {
  const lines: string[] = [
    `// Generated by ARIA compiler v0.1.0`,
    `// Source: ${kebab(module.name)}.aria`,
    `// DO NOT EDIT — regenerate with: aria gen ${kebab(module.name)}.aria`,
    ``,
    `import { z } from "zod";`,
    ``,
  ];

  for (const t of types) {
    if (t.docComment) {
      lines.push(`/** ${t.docComment} */`);
    }
    lines.push(...generateTypeDef(t));
    lines.push(``);
  }

  return lines.join("\n");
}

function generateTypeDef(t: TypeDef): string[] {
  const lines: string[] = [];
  const schemaName = t.name + "Schema";

  // Generic types: emit a plain TypeScript type alias without a Zod schema.
  // Zod does not support generic schemas cleanly — consumers instantiate the
  // generic type with concrete types when using it.
  if (t.typeParams && t.typeParams.length > 0) {
    const params = t.typeParams.join(", ");
    switch (t.base) {
      case "Record": {
        if (t.fields && t.fields.length > 0) {
          lines.push(`export type ${t.name}<${params}> = {`);
          for (const f of t.fields) {
            const fieldType = typeRefToTs(f.type);
            const optional = f.type.isOptional ? "?" : "";
            const comment = f.docComment ? ` // ${f.docComment}` : "";
            lines.push(`  ${f.name}${optional}: ${fieldType};${comment}`);
          }
          lines.push(`};`);
        } else {
          lines.push(`export type ${t.name}<${params}> = Record<string, unknown>;`);
        }
        break;
      }
      case "Enum": {
        if (t.variants && t.variants.length > 0) {
          const variantUnion = t.variants.map((v) => `{ kind: "${v.name}" }`).join(" | ");
          lines.push(`export type ${t.name}<${params}> = ${variantUnion};`);
        } else {
          lines.push(`export type ${t.name}<${params}> = never;`);
        }
        break;
      }
      default: {
        lines.push(`export type ${t.name}<${params}> = unknown;`);
        break;
      }
    }
    return lines;
  }

  switch (t.base) {
    case "Integer": {
      let schema = `z.number().int()`;
      for (const w of t.whereClauses) {
        schema += zodConstraintFromWhere(w, "number");
      }
      lines.push(`export const ${schemaName} = ${schema};`);
      lines.push(`export type ${t.name} = z.infer<typeof ${schemaName}>;`);
      break;
    }
    case "Decimal": {
      let schema = `z.number()`;
      for (const w of t.whereClauses) {
        schema += zodConstraintFromWhere(w, "number");
      }
      lines.push(`export const ${schemaName} = ${schema};`);
      lines.push(`export type ${t.name} = z.infer<typeof ${schemaName}>;`);
      break;
    }
    case "String": {
      let schema = `z.string()`;
      for (const w of t.whereClauses) {
        schema += zodConstraintFromWhere(w, "string");
      }
      lines.push(`export const ${schemaName} = ${schema};`);
      lines.push(`export type ${t.name} = z.infer<typeof ${schemaName}>;`);
      break;
    }
    case "Boolean": {
      lines.push(`export const ${schemaName} = z.boolean();`);
      lines.push(`export type ${t.name} = z.infer<typeof ${schemaName}>;`);
      break;
    }
    case "DateTime": {
      lines.push(`export const ${schemaName} = z.coerce.date();`);
      lines.push(`export type ${t.name} = z.infer<typeof ${schemaName}>;`);
      break;
    }
    case "Record": {
      {
        // Phase 7.2: separate storage fields from computed fields.
        // Storage fields go into the Zod schema; computed fields are emitted
        // as an intersection interface populated by the consumer or AI.
        const allFields = t.fields || [];
        const storageFields = allFields.filter((f) => !f.computed);
        const computedFields = allFields.filter((f) => f.computed);

        lines.push(`export const ${schemaName} = z.object({`);
        for (const f of storageFields) {
          const fieldSchema = typeRefToZod(f.type);
          const comment = f.docComment ? ` // ${f.docComment}` : "";
          lines.push(`  ${f.name}: ${fieldSchema},${comment}`);
        }
        lines.push(`});`);

        if (computedFields.length > 0) {
          lines.push(``);
          lines.push(`/**`);
          lines.push(` * Computed fields for ${t.name}.`);
          lines.push(` * Consumers are responsible for populating these derived values.`);
          lines.push(` */`);
          lines.push(`export interface ${t.name}Computed {`);
          for (const f of computedFields) {
            const sanitized = (f.computed || "").replace(/\*\//g, "* /").replace(/\n/g, " ");
            lines.push(`  /** @computed ${sanitized} */`);
            lines.push(`  ${f.name}: ${typeRefToTs(f.type)};`);
          }
          lines.push(`}`);
          lines.push(``);
          lines.push(`export type ${t.name} = z.infer<typeof ${schemaName}> & ${t.name}Computed;`);
        } else {
          lines.push(`export type ${t.name} = z.infer<typeof ${schemaName}>;`);
        }
      }
      break;
    }
    // end case Record
    case "Enum": {
      if (t.variants && t.variants.length > 0) {
        const values = t.variants.map((v) => `"${v.name}"`).join(", ");
        lines.push(`export const ${schemaName} = z.enum([${values}]);`);
        lines.push(`export type ${t.name} = z.infer<typeof ${schemaName}>;`);
        // Also export enum-like constants
        lines.push(`export const ${t.name} = {`);
        for (const v of t.variants) {
          const comment = v.docComment ? ` // ${v.docComment}` : "";
          lines.push(`  ${v.name}: "${v.name}" as const,${comment}`);
        }
        lines.push(`} as const;`);
      }
      break;
    }
    case "List": {
      const elemSchema = t.elementType ? `${t.elementType.name}Schema` : "z.unknown()";
      let schema = `z.array(${elemSchema})`;
      for (const w of t.whereClauses) {
        schema += zodConstraintFromWhere(w, "array");
      }
      lines.push(`export const ${schemaName} = ${schema};`);
      lines.push(`export type ${t.name} = z.infer<typeof ${schemaName}>;`);
      break;
    }
  }

  return lines;
}

function zodConstraintFromWhere(w: WhereClause, zodType: string): string {
  // Design decision: Zod constraints are generated from the raw expression string
  // via regex matching. The optional `w.parsedExpression` AST is available but
  // intentionally NOT consumed here — the string approach covers all current
  // patterns (gt, gte, lt, lte, length, regex, starts_with, compound and/or)
  // and is simpler to maintain. Migration to the expression AST is a v0.2 task
  // that would enable arbitrary computed Zod chains but requires a full tree walker.
  const expr = w.expression.trim();

  // Handle compound expressions: "self > 0 and self < 100"
  if (expr.includes(' and ')) {
    const parts = expr.split(' and ').map(p => p.trim());
    return parts.map(part => zodConstraintFromWhere({ kind: 'where', expression: part }, zodType)).join('');
  }
  if (expr.includes(' or ')) {
    // OR constraints can't be simply chained — use refine with actual logic
    const parts = expr.split(' or ').map(p => p.trim());
    const checks = parts.map(p => `/* ${p} */`).join(' || ');
    return `.refine((val) => { ${checks} return true; }, { message: ${JSON.stringify(expr)} })`;
  }

  // self > 0  (special case → .positive() for number, .min(1) for array)
  if (expr.match(/^self\s*>\s*0$/)) {
    if (zodType === "number") return `.positive()`;
    if (zodType === "array") return `.min(1)`;
  }

  // self >= 0  (special case → .nonnegative())
  if (expr.match(/^self\s*>=\s*0$/) && zodType === "number") {
    return `.nonnegative()`;
  }

  // self > N (supports negative numbers)
  const gtMatch = expr.match(/^self\s*>\s*(-?\d+)$/);
  if (gtMatch) {
    const val = parseInt(gtMatch[1]);
    return zodType === "number" ? `.gt(${val})` : "";
  }

  // self >= N (supports negative numbers)
  const gteMatch = expr.match(/^self\s*>=\s*(-?\d+)$/);
  if (gteMatch) {
    const val = parseInt(gteMatch[1]);
    return zodType === "number" ? `.gte(${val})` : "";
  }

  // self < N (supports negative numbers)
  const ltMatch = expr.match(/^self\s*<\s*(-?\d+)$/);
  if (ltMatch) {
    const val = parseInt(ltMatch[1]);
    return zodType === "number" ? `.lt(${val})` : "";
  }

  // self <= N (supports negative numbers and underscores)
  const lteMatch = expr.match(/^self\s*<=\s*(-?[\d_]+)$/);
  if (lteMatch) {
    const val = parseInt(lteMatch[1].replace(/_/g, ""));
    return zodType === "number" ? `.lte(${val})` : "";
  }

  // length(self) == N  (with optional spaces around parens)
  const lenEqMatch = expr.match(/^length\s*\(\s*self\s*\)\s*==\s*(\d+)$/);
  if (lenEqMatch) {
    const val = parseInt(lenEqMatch[1]);
    if (zodType === "string") return `.length(${val})`;
    if (zodType === "array") return `.length(${val})`;
    return "";
  }

  // length(self) <= N  (with optional spaces around parens)
  const lenLteMatch = expr.match(/^length\s*\(\s*self\s*\)\s*<=\s*([\d_]+)$/);
  if (lenLteMatch) {
    const val = parseInt(lenLteMatch[1].replace(/_/g, ""));
    if (zodType === "string") return `.max(${val})`;
    if (zodType === "array") return `.max(${val})`;
    return "";
  }

  // length(self) >= N  (with optional spaces around parens)
  const lenGteMatch = expr.match(/^length\s*\(\s*self\s*\)\s*>=\s*(\d+)$/);
  if (lenGteMatch) {
    const val = parseInt(lenGteMatch[1]);
    if (zodType === "string") return `.min(${val})`;
    if (zodType === "array") return `.min(${val})`;
    return "";
  }

  // self matches /regex/ — sanitize to prevent code injection
  const regexMatch = expr.match(/^self\s+matches\s+\/(.+)\/$/);
  if (regexMatch) {
    const safeRegex = regexMatch[1].replace(/\\/g, "\\\\");
    return zodType === "string" ? `.regex(new RegExp(${JSON.stringify(safeRegex)}))` : "";
  }

  // self starts_with "x" — sanitize to prevent code injection
  const startsMatch = expr.match(/^self\s+starts_with\s+"(.+)"$/);
  if (startsMatch) {
    return zodType === "string" ? `.startsWith(${JSON.stringify(startsMatch[1])})` : "";
  }

  // Unhandled constraint — refine with safe message
  const safeExpr = expr.replace(/\*\//g, "* /");
  return `.refine((val) => { /* TODO: ${safeExpr} */ return true; }, { message: ${JSON.stringify(expr)} })`;
}

function typeRefToTs(ref: { name: string; isOptional?: boolean; typeArgs?: Array<{ name: string }> }): string {
  const primitives: Record<string, string> = {
    Integer: "number",
    Decimal: "number",
    String: "string",
    Boolean: "boolean",
    DateTime: "Date",
  };
  // Handle generic instantiation: Result<Money, Error>
  if (ref.typeArgs && ref.typeArgs.length > 0) {
    const args = ref.typeArgs.map((a) => typeRefToTs({ name: a.name })).join(", ");
    return `${ref.name.split("<")[0]}<${args}>`;
  }
  return primitives[ref.name] ?? ref.name;
}

function typeRefToZod(ref: { name: string; isOptional?: boolean }): string {
  const primitives: Record<string, string> = {
    Integer: "z.number().int()",
    Decimal: "z.number()",
    String: "z.string()",
    Boolean: "z.boolean()",
    DateTime: "z.coerce.date()",
  };
  const base = primitives[ref.name] ?? `${ref.name}Schema`;
  return ref.isOptional ? `${base}.optional()` : base;
}

