function generateTestFile(module: AriaModule, contracts: ContractDef[]): string {
  const lines: string[] = [
    `// Generated by ARIA compiler v0.1.0`,
    `// Auto-generated tests from .aria examples`,
    `// Source: ${kebab(module.name)}.aria`,
    ``,
    `import { describe, it, expect } from "vitest";`,
    `import { ${contracts.map((c) => camelCase(c.name)).join(", ")} } from "./${kebab(module.name)}.contracts.js";`,
    ``,
  ];

  for (const c of contracts) {
    if (c.examples.length === 0) continue;

    lines.push(`describe("${c.name}", () => {`);
    for (let i = 0; i < c.examples.length; i++) {
      const ex = c.examples[i];
      const label = ex.name || `example ${i + 1}`;
      lines.push(`  it("${label}", async () => {`);
      lines.push(`    const input = ${JSON.stringify(ex.given, null, 6).replace(/\n/g, "\n    ")};`);
      lines.push(`    const result = await ${camelCase(c.name)}(input);`);
      for (const a of ex.then) {
        lines.push(`    // assert: ${a.expression}`);
      }
      lines.push(`    expect(result).toBeDefined();`);
      lines.push(`  });`);
      lines.push(``);
    }
    lines.push(`});`);
    lines.push(``);
  }

  return lines.join("\n");
}

